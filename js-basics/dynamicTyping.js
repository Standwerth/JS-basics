// Dynamic Typing

// There are two types of languages. Static-languages and Dynamic-languages.

// In a static-language, when you declare a varible, the type of that variable is set and can not be set in the future, i.e it is immutalbe.
// In constrast to a static-language, in a dynamic-language you can change the type of a variable

let score = "seven";
console.log(score);
console.log(typeof score); // The typeof operator tells us which data type a variable is.

// In a dynamic language like JavaScript you can chang the data type of a variable like this:
score = 7;
console.log(score);
console.log(typeof score);
